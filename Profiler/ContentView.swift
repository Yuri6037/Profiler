struct ContentView: View {
    @Environment(\.managedObjectContext) private var viewContext

    @FetchRequest(
        sortDescriptors: [NSSortDescriptor(keyPath: \Project.timestamp, ascending: true)],
        animation: .default)
    private var items: FetchedResults<Project>

    var body: some View {
        NavigationView {
            List {
                ForEach(items) { item in
                    NavigationLink {
                        Text("Item at \(item.timestamp!, formatter: dateFormatter)")
                        Text("Test: \(item.name!)")
                    } label: {
                        VStack(alignment: .leading) {
                            HStack {
                                Text("\(item.name!)").bold()
                                Text("(\(item.version ?? "No version"))")
                            }
                            Text("\(item.timestamp!, formatter: dateFormatter)")
                        }
                    }
                }
                .onDelete(perform: deleteItems)
            }
            Text("Select an item")
        }
    }

